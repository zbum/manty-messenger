export function getStickerById(stickerId) {
  for (const pack of stickerPacks) {
    const sticker = pack.stickers.find(s => s.id === stickerId)
    if (sticker) {
      return { ...sticker, packId: pack.id }
    }
  }
  return null
}

export function getStickerUrl(stickerId) {
  const sticker = getStickerById(stickerId)
  if (sticker) {
    return `/stickers/${sticker.packId}/${stickerId}.png`
  }
  return null
}
